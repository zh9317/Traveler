public class FlightDetailContentBean {
    private String price;
    private String moreService;
    private String order;
    private String cabinDiscount;
    private String returnOrModify;

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getMoreService() {
        return moreService;
    }

    public void setMoreService(String moreService) {
        this.moreService = moreService;
    }

    public String getOrder() {
        return order;
    }

    public void setOrder(String order) {
        this.order = order;
    }

    public String getCabinDiscount() {
        return cabinDiscount;
    }

    public void setCabinDiscount(String cabinDiscount) {
        this.cabinDiscount = cabinDiscount;
    }

    public String getReturnOrModify() {
        return returnOrModify;
    }

    public void setReturnOrModify(String returnOrModify) {
        this.returnOrModify = returnOrModify;
    }
}
